package org.dogoodthings.ectr.macro;

public class GenericExampleParameter
{
  private String p1;
  private String p2;
  private String p3;
  private String p4;
  public GenericExampleParameter(String p1, String p2, String p3, String p4)
  {
    this.p1 = p1;
    this.p2 = p2;
    this.p3 = p3;
    this.p4 = p4;
  }

  public String getP1()
  {
    return p1;
  }

  public String getP2()
  {
    return p2;
  }

  public String getP3()
  {
    return p3;
  }

  public String getP4()
  {
    return p4;
  }
}
